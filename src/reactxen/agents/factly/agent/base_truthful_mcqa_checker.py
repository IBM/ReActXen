def generate_groundedness_check_prompt(asset_name, claim_text, answer_text):
    prompt = """\
You are an expert fact-checking agent evaluating the factual accuracy of an answer generated by another language model to a multiple-choice question in the "{asset_name}" domain.

## Objective:
Determine whether the LLM-generated answer is factually grounded **based on your own internal knowledge**.

## Inputs:
- **Question Category**: "{asset_name}"
- **Question**: "{claim_text}"
- **LLM-Generated Answer**: "{answer_text}"

## Instructions:
1. Carefully analyze the provided answer using your internal understanding of general scientific and technical knowledge.
2. Identify key factual claims within the answer and determine whether they are likely to be:
   - **Supported** by common knowledge or known facts, or
   - **Contradicted** by known facts.
3. Provide a concise list of **supporting** and **contradicting** factual statements based on your internal knowledge.
4. Based on your evaluation, assign a **groundedness score** between 0.0 and 1.0:
   - 1.0 = Fully grounded (clearly supported by internal knowledge)
   - 0.5 = Mixed or uncertain grounding
   - 0.0 = Clearly ungrounded or contradicted
5. Include a **brief justification** summarizing why the score was given.

## Output Format:
Respond **only** with a valid JSON object in the following format (no extra commentary):

{{
  "action": "Finish",
  "action_input": {{
    "asset_type": "{asset_name}",
    "question": "{claim_text}",
    "answer": "{answer_text}",
    "supporting_facts": [
      "..."
    ],
    "contradicting_facts": [
      "..."
    ],
    "groundedness_score": 0.0,
    "justification": "..."
  }}
}}

Now generate the Response.
"""
    return prompt.format(asset_name=asset_name, claim_text=claim_text, answer_text=answer_text)

