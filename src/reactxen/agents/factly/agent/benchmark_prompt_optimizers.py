"""
benchmark_prompt_optimizers.py
------------------------------
A benchmarking suite to compare the system prompts generated by the three
different Prompt Optimization methods:
  1. V1 (Behavioral Only)
  2. V2 Lexical (Behavioral + Word-overlap Credit Assignment)
  3. V2 Semantic (Behavioral + Embedding-based Credit Assignment)

This script loads the optimized prompt from each method, runs the Factly agent
on a validation set of N questions from TruthfulQA.csv, and calculates
performance metrics to rank the methods.

Usage:
    python benchmark_prompt_optimizers.py --n 5
"""

from __future__ import annotations

import argparse
import json
import logging
import os
import random
import sys
import textwrap
from concurrent.futures import ThreadPoolExecutor, wait
from datetime import datetime
from functools import partial
from pathlib import Path
from typing import Any


_AGENT_DIR        = Path(__file__).resolve().parent
_FACTLY_DIR       = _AGENT_DIR.parent
_DATA_DIR         = _FACTLY_DIR / "data"
_OUTPUT_DIR       = _AGENT_DIR / "benchmark_outputs"
_ROOT_DIR         = _FACTLY_DIR.parents[3]

sys.path.insert(0, str(_ROOT_DIR))

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s – %(message)s",
)
logger = logging.getLogger(__name__)

def _run_validation_batch(
    prompt_text: str,
    sample: "pd.DataFrame",
    n_threads: int,
    batch_size: int,
) -> list[dict[str, Any]]:
    """Runs the ReactAgent on a dataset sample using the given system prompt."""
    try:
        from dotenv import load_dotenv
        load_dotenv()
        from reactxen.agents.react.agents import ReactAgent
        from langchain_community.agent_toolkits.load_tools import load_tools
        from langchain_community.tools.wikidata.tool import (
            WikidataAPIWrapper, WikidataQueryRun,
        )
        from langchain_community.tools.semanticscholar.tool import SemanticScholarQueryRun
        from reactxen.utils.model_inference import watsonx_llm
    except ImportError as exc:
        raise ImportError(f"Missing dependency: {exc}") from exc

    collected: dict[str, dict[str, Any]] = {}

    react_example = textwrap.dedent("""
        Question: I want to verify if 'Lubrication System Failure' in gearboxes
        can be monitored or detected using the 'Power Input' sensor.
        Thought 1: let me use wikipedia to search the content.
        Action 1: wikipedia
        Action Input 1: gearbox lubrication failure using power input sensor
        Observation 1: suppressed due to length
        Thought 2: Based on the evidence, I can now conclude the analysis.
        Action 2: Finish
        Action Input 2: Final Answer: { "asset_type": "gearbox", "claim": "...",
        "supporting_passages": [], "contradicting_passages": [],
        "groundedness_score": 0.8, "justification": "..." }
    """).strip()

    def _build_prompt(asset_name: str, claim_text: str, answer_text: str) -> str:
        if prompt_text == "BASELINE":
            from reactxen.agents.factly.agent.truthful_mcqa_checker import PROMPT_OG
            return PROMPT_OG.format(
                asset_name=asset_name,
                claim_text=claim_text,
                answer_text=answer_text
            )

        system_section = (
            f"[SYSTEM INSTRUCTIONS]\n{prompt_text}\n\n"
            if prompt_text.strip() else ""
        )
        task_prompt = textwrap.dedent(f"""
            Task: Fact-Check an LLM-Generated Answer to a Multiple-Choice Question.

            Objective: Determine whether the LLM-generated answer is factually
            grounded by identifying supporting or contradicting evidence from
            trusted sources.

            Inputs:
            - Question Category: "{asset_name}"
            - Multiple-Choice Question: "{claim_text}"
            - LLM-Generated Answer: "{answer_text}"

            Steps:
            1. Search Wikipedia, arXiv, DuckDuckGo, Wikidata, and Semantic Scholar.
            2. Extract the most relevant passages. Cite each as supporting or
               contradicting.
            3. Assign a groundedness score 0.0–1.0.
            4. End with a Finish action containing a valid JSON object.

            IMPORTANT: Respond only with a single valid JSON object with exactly
            two fields: "action" and "action_input". The "action" must be one of:
            arxiv, wikipedia, ddg-search, wikidata, semanticscholar, Finish.

            Final Output Format (for Finish only):
            {{
              "action": "Finish",
              "action_input": {{
                "asset_type": "{asset_name}",
                "question": "{claim_text}",
                "answer": "{answer_text}",
                "supporting_passages": [{{"text":"...","source":"...","highlight":"..."}}],
                "contradicting_passages": [{{"text":"...","source":"...","highlight":"..."}}],
                "groundedness_score": 0.0,
                "justification": "..."
              }}
            }}
        """).strip()
        return system_section + task_prompt

    def _process_row(row: Any) -> None:
        asset_name = row.get("Category", "unknown")
        entry_id   = row.get("question_id", "unknown")
        unique_key = f"{entry_id}_{asset_name}"
        question   = row.get("Question", "")
        answer     = row.get("Best Answer", "")
        incorrect  = row.get("Best Incorrect Answer", "")

        choices = [answer, incorrect]
        random.shuffle(choices)
        options_text = ", ".join([f"{chr(65+i)}. {c}" for i, c in enumerate(choices)])
        claim_text   = f"Q: {question}\nOptions: {options_text}"
        prompt       = _build_prompt(asset_name, claim_text, answer)

        try:
            wikidata         = WikidataQueryRun(api_wrapper=WikidataAPIWrapper())
            semantic_scholar = SemanticScholarQueryRun()
            tools = load_tools(["arxiv", "wikipedia", "ddg-search"])
            tools.extend([wikidata, semantic_scholar])

            selected_llm = partial(watsonx_llm, max_tokens=5000)
            ragent = ReactAgent(
                question=prompt,
                key="",
                cbm_tools=tools,
                tool_desc=None,
                max_steps=12,
                react_llm_model_id=16,
                react_example=react_example,
                handle_context_length_overflow=False,
                debug=True,
                apply_loop_detection_check=True,
                log_structured_messages=True,
                use_tool_cache=True,
                llm=selected_llm,
                enable_tool_partial_match=True,
                skip_token_counting=True,
            )
            ragent.run(name="benchmark_run")
            raw_answer = ragent.answer or ""

            try:
                traj_data = ragent.export_trajectory()
            except Exception:
                traj_data = {}

            steps = traj_data.get("trajectroy_log", traj_data.get("steps", []))

            groundedness_score = None
            try:
                cleaned = raw_answer.replace("Final Answer:", "").strip()
                parsed  = json.loads(cleaned)
                ai      = parsed.get("action_input", parsed)
                groundedness_score = float(ai.get("groundedness_score", 0.0))
            except Exception:
                pass

            collected[unique_key] = {
                "question":           question,
                "asset_name":         asset_name,
                "steps":              steps,
                "final_answer":       raw_answer,
                "groundedness_score": groundedness_score,
                "success":            True,
                "error":              None,
            }
        except Exception as exc:
            collected[unique_key] = {
                "question":           question,
                "asset_name":         asset_name,
                "steps":              [],
                "final_answer":       None,
                "groundedness_score": None,
                "success":            False,
                "error":              str(exc),
            }

    def _process_batch(batch: Any) -> None:
        for _, row in batch.iterrows():
            _process_row(row)

    batches = [sample.iloc[i: i + batch_size] for i in range(0, len(sample), batch_size)]
    with ThreadPoolExecutor(max_workers=n_threads) as executor:
        futures = [executor.submit(_process_batch, b) for b in batches]
        done, not_done = wait(futures, timeout=1800)
        for f in not_done:
            f.cancel()

    return list(collected.values())


def summarize_trajectory(traj: dict[str, Any]) -> dict[str, Any]:
    steps = traj.get("steps", [])

    tool_sequence = [
        step.get("action", "unknown")
        for step in steps
        if step.get("action") not in (None, "", "Final Answer", "Finish")
    ]

    repeated_tools = any(
        tool_sequence[i] == tool_sequence[i+1] == tool_sequence[i+2]
        for i in range(len(tool_sequence) - 2)
    ) if len(tool_sequence) >= 3 else False

    verify_kws = {"verify", "check", "cross", "validate", "confirm", "contradict"}
    missing_verification = not any(
        any(kw in (step.get("thought") or "").lower() for kw in verify_kws)
        for step in steps
    )

    premature_finish = len(tool_sequence) < 3 and traj.get("success", False)

    return {
        "n_steps":              len(steps),
        "groundedness_score":   traj.get("groundedness_score"),
        "success":              traj.get("success", False),
        "repeated_tool_calls":  repeated_tools,
        "missing_verification": missing_verification,
        "premature_finish":     premature_finish,
    }


def evaluate_method(
    method_name: str,
    prompt_path: Path | None,
    sample: Any,
    n_threads: int,
    batch_size: int,
) -> dict[str, Any] | None:
    if prompt_path is not None:
        if not prompt_path.exists():
            logger.warning("Prompt file %s not found. Skipping method %s.", prompt_path, method_name)
            return None
        prompt_text = prompt_path.read_text(encoding="utf-8")
    else:
        prompt_text = ""

    logger.info("Evaluating method: %s", method_name)

    trajectories = _run_validation_batch(prompt_text, sample, n_threads, batch_size)
    summaries = [summarize_trajectory(t) for t in trajectories]

    n   = len(summaries)
    if n == 0:
        return None

    success_rate = sum(1 for s in summaries if s["success"]) / n
    scores = [s["groundedness_score"] for s in summaries if s["groundedness_score"] is not None]
    avg_score = sum(scores) / len(scores) if scores else 0.0

    fault_rate = sum(1 for s in summaries if s["repeated_tool_calls"] or s["missing_verification"] or s["premature_finish"]) / n
    avg_steps = sum(s["n_steps"] for s in summaries) / n

    # Composite Score: Higher is better
    # Weightings: 40% Success, 40% Groundedness, -20% Faults (Penalty)
    composite_score = (success_rate * 0.4) + (avg_score * 0.4) - (fault_rate * 0.2)
    # Normalize to 0-100 scale for readability
    composite_score = max(0.0, composite_score) * 100

    return {
        "method_name": method_name,
        "success_rate": success_rate,
        "avg_groundedness": avg_score,
        "fault_rate": fault_rate,
        "avg_steps": avg_steps,
        "composite_score": composite_score
    }


def main():
    parser = argparse.ArgumentParser(description="Benchmark Prompt Optimizers")
    parser.add_argument("--n", type=int, default=5, help="Number of validation questions to run.")
    parser.add_argument("--threads", type=int, default=8, help="Worker threads.")
    parser.add_argument("--batch-size", type=int, default=5, help="Batch size.")
    parser.add_argument("--v1-prompt", type=Path, default=_ROOT_DIR/"genai-proj-results"/"prompt_optimizer_v1"/"system_prompt.txt")
    parser.add_argument("--v2-lexical-prompt", type=Path, default=_ROOT_DIR/"genai-proj-results"/"prompt_optimizer_v2_default"/"system_prompt.txt")
    parser.add_argument("--v2-semantic-prompt", type=Path, default=_ROOT_DIR/"genai-proj-results"/"prompt_optimizer_v2_semantic"/"system_prompt.txt")
    parser.add_argument(
        "--methods", 
        type=str, 
        default="all", 
        help="Comma-separated list of methods to run: baseline, v1, v2_lexical, v2_semantic. Default is 'all'."
    )
    
    args = parser.parse_args()

    import pandas as pd
    csv_path = _DATA_DIR / "TruthfulQA.csv"
    data = pd.read_csv(csv_path)
    data["question_id"] = list(range(len(data)))
    sample = data.sample(n=min(args.n, len(data)), random_state=42).reset_index(drop=True)

    all_methods = [
        ("baseline", "Baseline (No Optimization)", None),
        ("v1", "V1_Behavioral", args.v1_prompt),
        ("v2_lexical", "V2_Lexical", args.v2_lexical_prompt),
        ("v2_semantic", "V2_Semantic", args.v2_semantic_prompt),
    ]

    selected_keys = [m.strip().lower() for m in args.methods.split(",")]
    if "all" in selected_keys:
        methods = [(name, path) for key, name, path in all_methods]
    else:
        methods = [(name, path) for key, name, path in all_methods if key in selected_keys]

    if not methods:
        logger.error("No valid methods selected. Choose from: baseline, v1, v2_lexical, v2_semantic.")
        sys.exit(1)

    results = []
    for name, path in methods:
        res = evaluate_method(name, path, sample, args.threads, args.batch_size)
        if res:
            results.append(res)

    if not results:
        logger.error("No valid prompt files found to benchmark. Please ensure the prompts exist.")
        sys.exit(1)

    # Rank by composite score
    results.sort(key=lambda x: x["composite_score"], reverse=True)

    _OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    methods_str = args.methods.replace(",", "_")
    out_file = _OUTPUT_DIR / f"prompt_benchmark_summary_n={args.n}_{methods_str}.json"
    
    with out_file.open("w", encoding="utf-8") as f:
        json.dump({
            "num_validation_questions": args.n,
            "winner": results[0]["method_name"],
            "rankings": results
        }, f, indent=2)

    logger.info("Benchmarking complete! Winner: %s", results[0]["method_name"])
    logger.info("Results saved to %s", out_file)


if __name__ == "__main__":
    main()
